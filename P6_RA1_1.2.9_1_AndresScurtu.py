# Descripció: Indica si el número és positiu, negatiu o zero.
# Especificacions de l'entrega: Escriu un programa que demani a l’usuari un número , digui si és positiu, negatiu o zero i imprimeixi “Gràcies!” al final.


# Demanem un número a l'usuari
numero = float(input("Introdueix un número: "))

# Comprovem si és positiu, negatiu o zero
if numero > 0:
    print("El número és positiu.")
elif numero < 0:
    print("El número és negatiu.")
else:
    print("El número és zero.")

# Missatge de comiat
print("Gràcies!")






