# Definir la funció que retorna el màxim de tres nombres
def maxim(a, b, c):
    return max(a, b, c)

# Crides a la funció amb trios de nombres
print(maxim(3, 7, 5))    # Sortida: 7
print(maxim(10, 2, 8))   # Sortida: 10
print(maxim(1, 1, 1))    # Sortida: 1
