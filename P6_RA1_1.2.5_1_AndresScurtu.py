# Descripció: Demana el nom i el mostra amb una funció.
# Especificacions de l'entrega: Entrada, funció i sortida.

INCREMENT = 5  # a. Constant

def suma(a, b):  # b. Funció
    return a + b + INCREMENT  # Fa servir la constant

x = int(input("Introdueix un número: "))  # c. Entrada de l’usuari
y = int(input("Introdueix un altre número: "))  # c. Entrada de l’usuari

print("La suma més l'increment és:", suma(x, y))  # d. Sortida per pantalla





