# Definir la funció per sumar els nombres de la llista
def suma_nombres(lista):
    return sum(lista)

# Exemple de llista de números
numeros = [1, 2, 3, 4, 5]

# Cridar la funció i mostrar el resultat
resultat = suma_nombres(numeros)
print("La suma de tots els números és:", resultat)
